    SELECT 1 FROM sys.columns
    WHERE object_id = OBJECT_ID('ChiTietHoaDon') AND name = 'GiaGoc'
)
    ALTER TABLE ChiTietHoaDon ADD GiaGoc FLOAT;
GO

